function getRandomInt(min, max) {
    min = Math.ceil(min);
    max = Math.floor(max);
    return Math.floor(Math.random() * (max - min) + min);
}

let i = 0;

while (i != 5) {
    i = getRandomInt(1, 10);
    console.log(i);
}